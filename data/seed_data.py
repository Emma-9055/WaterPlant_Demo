def get_plant_by_name(name: str) -> dict | None:
    """按水厂名称模糊匹配，返回水厂信息 dict"""
    name_lower = name.strip().lower()
    for plant in PLANTS:
        if name_lower in plant["name"].lower() or plant["name"].lower() in name_lower:
            return plant
    return None


def get_category_by_name(name: str) -> dict | None:
    """按类别名称查找"""
    for cat in CATEGORIES:
        if cat["name"] == name:
            return cat
    return None


def build_search_texts() -> list[dict]:
    """
    构建可入库的检索文本列表。
    每条历史案例和知识文档都转为可被向量检索的文本，
    返回 [{"id": str, "text": str, "type": "case"|"doc", "metadata": dict}]
    """
    texts = []

    # 历史案例 → 检索文本
    for case in HISTORICAL_CASES:
        text = (
            f"【{case['category']}】{case['plant_name']}：{case['description']}"
        )
        texts.append({
            "id": case["id"],
            "text": text,
            "type": "case",
            "metadata": {
                "plant_name": case["plant_name"],
                "category": case["category"],
                "urgency": case["urgency"],
                "resolution": case["resolution"],
                "date": case["date"],
            },
        })

    # 知识文档 → 检索文本
    for doc in KNOWLEDGE_DOCS:
        text = f"{doc['title']}：{doc['content']}"
        texts.append({
            "id": doc["id"],
            "text": text,
            "type": "doc",
            "metadata": {
                "title": doc["title"],
            },
        })

    return texts


